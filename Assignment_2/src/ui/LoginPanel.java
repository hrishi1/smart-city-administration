    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        // TODO add your handling code here:
        
        uname = txtUname.getText();
        password = txtPassword.getText();
        
        //Radio buttons
        
        if(radioComAd.isSelected() == true) {
            userType = "Community Admin";
        }
        else if(radioSysAd.isSelected() == true) {
            userType = "System Admin";
        }        
        else if(radioHosAd.isSelected() == true) {
            userType = "Hospital Admin";
        }
//        else if(radioPatient.isSelected() == true) {
//            userType = "Patient";
//        }
//        else if(radioDoctor.isSelected() == true) {
//            userType = "Doctor";
//        }                
        
        ArrayList<UserAuth> uDir = uaDir.getUserAuthDir();
        
        //System.out.println("Uname: "+userType);
        
        boolean authFlag = false;
        
        for(UserAuth u : uDir) {
            
              // Test
//            System.out.println("Uname: "+u.getUserName());
//            System.out.println("pass: "+u.getPassword());
//            System.out.println("type: "+u.getUserType());
//            
//            System.out.println("Uname: "+uname);
//            System.out.println("pass: "+password);
//            System.out.println("type: "+userType);
//            
//            System.out.println(u.getUserType() == userType);
//            System.out.println(u.getUserName().equalsIgnoreCase(uname));
//            System.out.println(u.getPassword().equalsIgnoreCase(password));
            
            if(u.getUserType().equalsIgnoreCase(userType) && u.getUserName().equalsIgnoreCase(uname) && u.getPassword().equalsIgnoreCase(password)) {
                
                
                authFlag = true;
                
                if (userType.equalsIgnoreCase("Doctor")) {
                    
                }
                else if (userType.equalsIgnoreCase("Patient")) {
                    
                   
                    
                }
                else if (userType.equalsIgnoreCase("System Admin")) {
                    
                    SySAdminPanel s = new SySAdminPanel(cityDir, uaDir, splitPane);//cityDir,uaDir);
                    
                    splitPane.setRightComponent(s);
                    
                    
                }
                else if (userType.equalsIgnoreCase("Community Admin")) {
                    
                    CAModifyHousesPanel s = new CAModifyHousesPanel(cityDir, uaDir, splitPane);//cityDir,uaDir);
                                        
                    splitPane.setRightComponent(s);
                    
                }
                else if (userType.equalsIgnoreCase("Hospital Admin")) {
                    
                    HAModifyPanel s = new HAModifyPanel(cityDir, uaDir, splitPane);//cityDir,uaDir);
                                        
                    splitPane.setRightComponent(s);
                    
                }
                
            }
            
        }
        
        
        if(authFlag == false) {
            JOptionPane.showMessageDialog(this, "Authentication failed.");
        }
        
        
        
        
    }//GEN-LAST:event_btnLoginActionPerformed

